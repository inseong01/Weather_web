const mergeWeekData = (data, currentDate) => {
  let num = 1;
  const weekArr = new Map();

  // 최저/고온도(내일, 모레) -> taMin1 ~ 
  const threeDaysBefore = data.before.filter(item => item.fcstDate !== currentDate && item.category.match(/TMN|TMX/));

  // 객체 변환
  const resultArray = new Map();
  data.threeDaysLater.forEach(content =>
    Object.keys(content).forEach((key) => resultArray.set(key, Number(content[key]).toFixed(0)))
  )

  threeDaysBefore.forEach(item => {
    if (item.category === 'TMN') {
      weekArr.set(`taMin${num}`, Number(item.fcstValue).toFixed(0));
    } else if (item.category === 'TMX') {
      weekArr.set(`taMax${num}`, Number(item.fcstValue).toFixed(0));
      num++
    }
  })

  // 정렬
  const arr = new Map([...weekArr, ...resultArray]);
  const filteredArr = new Map();
  arr.forEach((value, key) => { // value key가 반대임
    if (key.match(/ta(Max|Min)\d*$/)) filteredArr.set(key, value);
  });

  // 합병
  const collectTemp = [];
  const sixArray = 7;
  for (let i = 1; i < sixArray; i++) {
    const mergeArr = {
      taMin: filteredArr.get(`taMin${i}`),
      taMax: filteredArr.get(`taMax${i}`)
    }
    collectTemp.push(mergeArr) // {taMin, taMax}
  }

  return collectTemp
}

export default mergeWeekData;